package NaveenAutomationLabs;

public class SwapIntegersWithoutUsingThirdVarSeven {
	
	
	public static void main(String[] args) {
		int x = 50;
		int y= 100;
		
		x = x+y;
		y= x-y;
		x = x-y;
		
		System.out.println("Value of y is : " + y);
		System.out.println("Value of x is : " + x);
		
		
		
	}

	

}
